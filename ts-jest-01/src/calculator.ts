export class Calculator {
  public plus(left: number, right: number) {
    return left + right;
  }

  public minus(left: number, right: number) {
    return left - right;
  }

  public multiple(left: number, right: number) {
    return left * right;
  }

  public divide(left: number, right: number) {
    return left / right;
  }
}